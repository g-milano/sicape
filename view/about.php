<h2 align="right" class="categories"><b><span> ¿QUIENÉS SOMOS?</span></b></h2>
<p>

<div style="text-align: justify"> Capacitación y Asesoría por Profesionales Especializados,
	 C.A.P.E. R.L., es una Asociación Cooperativa, conformada por profesionales de diferentes
	  áreas con experiencia laboral y solvencia moral comprobada, que basa su Filosofía de 
	  Gestión en los principios y valores cristianos. Nuestro comportamiento se rige por la 
	  Honestidad, Responsabilidad, Compromiso y Hermandad, que nos permitirán:</p></div>
 <br/>
<ul style="text-align: justify">	
	<li>Contribuir con la formación y actualización del capital humano que le permita a la 
		comunidad empresarial contar con un cliente en el desarrollo de su recurso que los 
		convierta en más eficientes y eficaces en el logro de sus objetivos y metas trazadas.</li><br />
	<li>Asesorar a nuestros clientes en materia Administrativa, Contable, Laboral, Legal, 
		Financiera, Ambiental, Seguridad, Motivación Humana e Ingeniería,  hasta alcanzar su 
		más eficiente desarrollo humano y organizacional en la búsqueda de la excelencia 
		empresarial.</li> <br />
	<li>Proveer servicios de alta calidad que garantice a nuestros clientes, tranquilidad y
		 estabilidad en el logro de las actividades planteadas.</li><br />
</ul>

<h2 align="right"><b><span> MISION</span></b></h2>
<p>
	<div style="text-align: justify">Proporcionar a la comunidad empresarial, laboral y 
		estudiantil, herramientas necesarias que contribuyan con su desarrollo y crecimiento,
		 con capacitación, adiestramiento, servicios y asesorías, tanto en lo personal como 
		 en lo  profesional en todas las áreas del conocimiento,  utilizando para ello personal 
		 con experiencia laboral, con modernas técnicas de formación y recursos tecnológicos y
		  didácticos eficientes.
	</div>
</p>
<h2 align="right"><b><span> VISION</span></b></h2>
<p>
	<div style="text-align: justify"> Convertirnos en una organización de referencia en 
		los ámbitos en que participamos, ofreciendo servicios de  calidad y a plena satisfacción 
		de nuestros clientes, apoyándolos permanentemente en la búsqueda de la excelencia personal,
		 profesional y organizacional
		
	</div>
</p>
